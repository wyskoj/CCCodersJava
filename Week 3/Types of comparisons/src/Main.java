@SuppressWarnings("ConstantConditions")
public class Main {
   
   public static void main(String[] args) {
      // We can compare numbers using "logical operators"
      int x = 3;
      int y = 4;
      System.out.println(x == y);  // false
      System.out.println(x < y);   // true
      System.out.println(x > y);   // false
      System.out.println(x <= y);  // true
      System.out.println(x >= y);  // false
      System.out.println(x != y);  // true
   }
}